function extractFunction(source, name) {
  const markers = [`async function ${name}(`, `function ${name}(`];
  const start = markers
    .map((marker) => source.indexOf(marker))
    .find((index) => index >= 0);
  if (start < 0) {
    throw new Error(`missing function ${name}`);
  }

  let parenDepth = 0;
  let signatureEnded = false;
  let braceStart = -1;
  for (let i = start; i < source.length; i += 1) {
    const ch = source[i];
    if (ch === '(') {
      parenDepth += 1;
    } else if (ch === ')') {
      parenDepth -= 1;
      if (parenDepth === 0) {
        signatureEnded = true;
      }
    } else if (ch === '{' && signatureEnded) {
      braceStart = i;
      break;
    }
  }

  let depth = 0;
  let end = braceStart;
  for (; end < source.length; end += 1) {
    const ch = source[end];
    if (ch === '{') depth += 1;
    if (ch === '}') {
      depth -= 1;
      if (depth === 0) {
        end += 1;
        break;
      }
    }
  }

  return source.slice(start, end);
}

test('throwIfStopped surfaces AUTO_RUN_STEP_IDLE_RESTART when step-idle watchdog trips', () => {
  const api = new Function(`
const STOP_ERROR_MESSAGE = '流程已被用户停止。';
let stopRequested = false;
function throwIfAutoRunStageStalled() {}
function throwIfAutoRunStepIdleRestart() {
  const error = new Error('AUTO_RUN_STEP_IDLE_RESTART::step 4 idle for 120s');
  error.code = 'AUTO_RUN_STEP_IDLE_RESTART';
  error.step = 4;
  throw error;
}
${extractFunction(backgroundSource, 'throwIfStopped')}
return {
  run() {
    throwIfStopped();
  },
};
`)();

  assert.throws(
    () => api.run(),
    (error) => String(error?.code || '') === 'AUTO_RUN_STEP_IDLE_RESTART'
      && Number(error?.step) === 4
  );
});

